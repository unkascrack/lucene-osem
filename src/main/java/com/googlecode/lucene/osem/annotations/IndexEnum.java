package com.googlecode.lucene.osem.annotations;

public enum IndexEnum {

    /**
     * Use default value
     */
    NA,

    /**
     * Set "index" to "analyzed"
     */
    ANALYZED,

    /**
     * Set "index" to "not_analyzed"
     */
    NOT_ANALYZED,

    /**
     * Set "index" to "no"
     */
    NO
}
